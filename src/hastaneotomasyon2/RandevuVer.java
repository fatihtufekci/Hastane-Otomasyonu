    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String doktor = (String)jComboBox2.getSelectedItem();
        String bolum = (String)jComboBox1.getSelectedItem();
        int dene = 0;
        String ad;
        String soyad;
        char j = ' ';
        int count = 0;
        for(int i=0; i<doktor.length(); i++){
            j = doktor.charAt(i);
            if(j==' '){
                count++;
            }
        }
        if(count==2){
            dene=doktor.lastIndexOf(" ");
        }
        else if(count==1){
            dene=doktor.indexOf(" ");
        }
        ad = doktor.substring(0, dene);
        soyad = doktor.substring(dene+1, doktor.length());
        
        String query3 = "select doktorTC, tarih, saatID from randevu";
        
        try {
            psmt = conn.prepareStatement(query3);
            rs = psmt.executeQuery();
            
            while(rs.next()){
                dTC = rs.getString("doktorTC");
                t = rs.getString("tarih");
                sID = rs.getString("saatID");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
        
        String errorMessage = "";
        int correctNumber = 0;
        int numFields = 3;
        
        // check year
        try{
            int yy = Integer.parseInt(jTextField4.getText());
            if(yy<2017 || yy>2018){
                errorMessage = errorMessage.concat("Lütfen doğru yıl değerleri giriniz! -");
            }else{
                correctNumber++;
                year = yy;
            }
        }catch(NumberFormatException ex){
            errorMessage = errorMessage.concat("Yıl geçerli bir sayı değil! -");
        }
        
        // check month
        try{
            int mm = Integer.parseInt(jTextField2.getText());
            if(mm<1 || mm>12){
                errorMessage = errorMessage.concat("Lütfen doğru ay değerleri giriniz! -");
            }else{
                correctNumber++;
                month = mm;
            }
        }catch(NumberFormatException ex){
            errorMessage = errorMessage.concat("Ay geçerli bir sayı değil! -");
        }
        
        //check day
        try{
            int dd = Integer.parseInt(jTextField5.getText());
            if(dd<1 || dd>31){
                errorMessage = errorMessage.concat("Lütfen doğru gün değerleri giriniz! -");
            }else{
                correctNumber++;
                day = dd;
            }
        }catch(NumberFormatException ex){
            errorMessage = errorMessage.concat("Gün geçerli bir sayı değil! -");
        }
        
        if(correctNumber < numFields){
            JOptionPane.showMessageDialog(null, errorMessage, "geçersiz/eksik veri girildi", JOptionPane.ERROR_MESSAGE);
        }else{
            String query = "insert into randevu(tarih, hastaTC, doktorTC, saatID, muayeneEdildimi) values(?, ?, ?, ?, ?)";
            String query2 = "select doktorTC from bolumdoktor where ad=? and soyad=? and bolumAdi=?";
            long doktorTC=0L;
            tarihDate = String.valueOf(year)+"-"+String.valueOf(month)+"-"+String.valueOf(day);
            
            try {
                psmt = conn.prepareStatement(query2); 
            
                psmt.setString(1, ad);
                psmt.setString(2, soyad);
                psmt.setString(3, bolum);
            
                rs = psmt.executeQuery();
                String de = "";
                while(rs.next()){
                    de = rs.getString("doktorTC");
                }
            
                doktorTC = Long.parseLong(de);
            
                psmt.close();
                rs.close();
                
                if(dTC.equals(String.valueOf(doktorTC)) && t.equals(tarihDate) && sID.equals(jTextField3.getText())){
                    JOptionPane.showMessageDialog(null, "Randevu dolu alınamaz! ");
                    jTextField3.setText("");
                }else{
                    ekle(query, doktorTC);
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

