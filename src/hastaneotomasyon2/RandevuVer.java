    private void bolumleriYukle() {
        
        String sql = "select * from bolum";
        
        try {
            psmt = conn.prepareStatement(sql);
            rs = psmt.executeQuery();
            
            while(rs.next()){
                jComboBox1.addItem(rs.getString("bolumAdi"));
            }
            
            rs.close();
            psmt.close();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }

    private void doktorlariComboyaEkle() {
        
        String sql = "select ad, soyad from bolumdoktor where bolumAdi = ?";
            
        jComboBox2.removeAllItems();
        
        try {
            psmt = conn.prepareStatement(sql);
            psmt.setString(1, (String)jComboBox1.getSelectedItem());
            
            rs = psmt.executeQuery();
            
            while(rs.next()){
                jComboBox2.addItem(rs.getString("ad") + " " + rs.getString("soyad"));
            }
            
            rs.close();
            psmt.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }

