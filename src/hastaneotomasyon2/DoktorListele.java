    private void doktorlariListele() {
        String sql = "select * from doktorbolum";
        
        try {
            psmt = conn.prepareStatement(sql);
            rs = psmt.executeQuery();
            //dtm = new DefaultTableModel();
            jTable1.setModel(dtm);
            dtm.addColumn("doktorTC");
            dtm.addColumn("ad");
            dtm.addColumn("soyad");
            dtm.addColumn("telefon");
            dtm.addColumn("adres");
            dtm.addColumn("Doğum Tarihi");
            dtm.addColumn("Doğum Yeri");
            dtm.addColumn("Branşı");
            dtm.addColumn("Cinsiyet");
            dtm.addColumn("Ücreti");
            
            while(rs.next()){
                dtm.addRow(new String[]{rs.getString("doktorTC"),rs.getString("ad"),rs.getString("soyad"),rs.getString("telefon"), rs.getString("adres"),rs.getString("dogumTarihi"),rs.getString("dogumYeri"),rs.getString("bolumAdi"),rs.getString("cinsiyet"),rs.getString("ucret")});
            }
            
            rs.close();
            psmt.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
